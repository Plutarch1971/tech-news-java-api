package com.technews;

public class VariableTypes {
    public static void main(String[] args) {
        String name = "Cleopetra";
        int age = 2016 + 69;
        double price = 1999.99;
        boolean truth = true;
        boolean falseness = !truth;
        System.out.println(name + " " + age + " " + price + " " + falseness);
    }
}
